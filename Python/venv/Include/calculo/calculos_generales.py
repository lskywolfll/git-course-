
def sumar(op1, op2):
    print("El resulado de la suma es: ", op1 + op2)

def restar(op1, op2):
    print("El resultado de la resta es: ", op1 - op2)

def multiplicacion(op1, op2):
    print("El resultado de la multiplicacion es:", op1 * op2)

def dividir(dividendo, divisor):
    print("El resuldado de la divicion es: ", dividendo/divisor)

def potencia(base,exponente):
    print("El resulado de la potencia es: ", base**exponente)

def redondear(numero):
    print("El resultado del redondeo es: ", round(numero))